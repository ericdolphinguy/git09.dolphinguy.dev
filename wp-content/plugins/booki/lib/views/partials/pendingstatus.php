<p>This variable is set only if payment_status = Pending.</p>
<div>
<strong>address:</strong> The payment is pending because your customer did not include a confirmed shipping address and your
Payment Receiving Preferences is set to allow you to manually accept or deny each of these payments. To change your preference,
go to the Preferences section of your Profile.</div>
<div>
<strong>authorization:</strong> You set the payment action to Authorization and have not yet captured funds.</div>
<div>
<strong>echeck:</strong> The payment is pending because it was made by an eCheck that has not yet cleared.</div>
<div>
<strong>intl:</strong> The payment is pending because you hold a non-U.S. account and do not have a withdrawal mechanism. You
must manually accept or deny this payment from your Account Overview.</div>
<div>
<strong>multi-currency:</strong> You do not have a balance in the currency sent, and you do not have your Payment Receiving
Preferences set to automatically convert and accept this payment. You must manually accept or deny this payment.</div>
<div>
<strong>order:</strong> You set the payment action to Order and have not yet captured funds.</div>
<div>
<strong>paymentreview:</strong> The payment is pending while it is being reviewed by PayPal for risk.</div>
<div>
<strong>unilateral:</strong> The payment is pending because it was made to an email address that is not yet registered or
confirmed.</div>
<div>
<strong>upgrade:</strong> The payment is pending because it was made via credit card and you must upgrade your account to
Business or Premier status in order to receive the funds. upgrade can also mean that you have reached the monthly limit for
transactions on your account.</div>
<div>
<strong>verify:</strong> The payment is pending because you are not yet verified. You must verify your account before you can
accept this payment.</div>
<div>
<strong>other:</strong> The payment is pending for a reason other than those listed above. For more information, contact PayPal
Customer Service.</div>
